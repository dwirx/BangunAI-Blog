import fs from "node:fs/promises";
import path from "node:path";
import yaml from "js-yaml";

const ROOT = process.cwd();
const CONTENT_DIR = path.join(ROOT, "src", "content");
const OUTPUT_FILE = path.join(CONTENT_DIR, "content-index.generated.ts");

function asString(value, fallback = "") {
  if (typeof value === "string") return value;
  if (value == null) return fallback;
  return String(value);
}

function asNumber(value, fallback = 0) {
  const parsed = typeof value === "number" ? value : Number(value);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function asBoolean(value) {
  return typeof value === "boolean" ? value : undefined;
}

function asStringArray(value) {
  if (!Array.isArray(value)) return [];
  return value.map((item) => asString(item).trim()).filter(Boolean);
}

function slugFromFilename(filename) {
  return filename.replace(/\.mdx$/i, "");
}

async function readFrontmatter(filePath) {
  const raw = await fs.readFile(filePath, "utf8");
  const match = raw.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const parsed = yaml.load(match[1]);
  return parsed && typeof parsed === "object" ? parsed : {};
}

async function listMdxFiles(dirPath) {
  const entries = await fs.readdir(dirPath, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".mdx"))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

async function buildPostMeta(sectionDir, defaultType, defaultCategory) {
  const dirPath = path.join(CONTENT_DIR, sectionDir);
  const files = await listMdxFiles(dirPath);
  const result = [];
  for (const file of files) {
    const fm = await readFrontmatter(path.join(dirPath, file));
    const slug = asString(fm.slug, slugFromFilename(file)).trim();
    const item = {
      path: `./${sectionDir}/${file}`,
      slug,
      title: asString(fm.title, slug),
      summary: asString(fm.summary, ""),
      type: asString(fm.type, defaultType),
      category: asString(fm.category, defaultCategory),
      tags: asStringArray(fm.tags),
      date: asString(fm.date, slug),
      readingTime: asNumber(fm.readingTime, 6),
    };
    const featured = asBoolean(fm.featured);
    if (featured !== undefined) {
      item.featured = featured;
    }
    result.push(item);
  }
  return result;
}

async function buildReadMeta() {
  const sectionDir = "read";
  const dirPath = path.join(CONTENT_DIR, sectionDir);
  const files = await listMdxFiles(dirPath);
  const result = [];
  for (const file of files) {
    const fm = await readFrontmatter(path.join(dirPath, file));
    const slug = asString(fm.slug, slugFromFilename(file)).trim();
    const item = {
      path: `./${sectionDir}/${file}`,
      slug,
      title: asString(fm.title, slug),
      snippet: asString(fm.snippet, ""),
      source: asString(fm.source, ""),
      url: asString(fm.url, ""),
      tags: asStringArray(fm.tags),
      date: asString(fm.date, slug),
    };
    const hasBody = asBoolean(fm.hasBody);
    if (hasBody !== undefined) {
      item.hasBody = hasBody;
    }
    result.push(item);
  }
  return result;
}

async function buildDailyMeta() {
  const sectionDir = "daily";
  const dirPath = path.join(CONTENT_DIR, sectionDir);
  const files = await listMdxFiles(dirPath);
  const result = [];
  for (const file of files) {
    const fm = await readFrontmatter(path.join(dirPath, file));
    const fallbackSlug = slugFromFilename(file);
    const slug = asString(fm.slug, fallbackSlug).trim();
    const date = asString(fm.date, slug).trim();
    result.push({
      path: `./${sectionDir}/${file}`,
      slug,
      title: asString(fm.title, `Daily Note: ${date}`).trim(),
      date,
      summary: asString(fm.summary, `Catatan harian ${date}`).trim(),
      tags: asStringArray(fm.tags),
    });
  }
  return result;
}

async function buildSingleMeta(filename) {
  const fullPath = path.join(CONTENT_DIR, filename);
  try {
    await fs.access(fullPath);
  } catch {
    return null;
  }
  const fm = await readFrontmatter(fullPath);
  return {
    path: `./${filename}`,
    frontmatter: fm,
  };
}

function toTsLiteral(name, value) {
  return `export const ${name} = ${JSON.stringify(value, null, 2)};\n`;
}

async function main() {
  const writingMeta = await buildPostMeta("writing", "essay", "Tech");
  const articleMeta = await buildPostMeta("articles", "article", "Tech");
  const readMeta = await buildReadMeta();
  const dailyMeta = await buildDailyMeta();
  const aboutMeta = await buildSingleMeta("about.mdx");
  const nowMeta = await buildSingleMeta("now.mdx");

  const header = [
    "// Auto-generated by scripts/generate-content-index.mjs",
    "// Do not edit manually.",
    "",
  ].join("\n");

  const body =
    toTsLiteral("writingMeta", writingMeta) +
    toTsLiteral("articleMeta", articleMeta) +
    toTsLiteral("readMeta", readMeta) +
    toTsLiteral("dailyMeta", dailyMeta) +
    toTsLiteral("aboutMeta", aboutMeta) +
    toTsLiteral("nowMeta", nowMeta);

  await fs.writeFile(OUTPUT_FILE, `${header}${body}`, "utf8");
  process.stdout.write(`Generated ${path.relative(ROOT, OUTPUT_FILE)}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
